            logging.info(f"–°–æ–∑–¥–∞–Ω–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏—è: {name} (—Ä–æ–¥–∏—Ç–µ–ª—å: {parent.name if parent else '–Ω–µ—Ç —Ä–æ–¥–∏—Ç–µ–ª—è'})")
